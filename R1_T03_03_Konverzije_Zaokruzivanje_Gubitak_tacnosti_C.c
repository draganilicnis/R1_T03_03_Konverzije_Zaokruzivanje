#include <stdio.h>
int main(void)
{
    printf("%.*g\n", 17, 0.1 + 0.2);             // 0.30000000000000004
    printf("%.*g\n", 17, 0.3 / 0.1);             // 2.9999999999999996
    printf("%.*g\n", 17, 0.1 + 0.2 - 0.3);       // 5.5511151231257827e-17
    printf("%.*g\n", 17, 3 - 0.3 / 0.1);         // 4.4408920985006262e-16
    printf("%.*g\n", 17, 9007199254740993.0);    // 9007199254740992
    printf("%.*g\n", 17, 9007199254740993.0 - 9007199254740992.0); // 0
    printf("%lld\n", 9007199254740993LL - 9007199254740992LL);     // 1
    printf("%.2f\n", 9007199254740993.0);        // 9007199254740992.00
    printf("%.*g\n", 17, 1.0/10 + 2.0/10 - 3.0/10);       // 5.5511151231257827e-17
    printf("%.*g\n", 17, 1/10.0 + 2/10.0 - 3/10.0);       // 5.5511151231257827e-17
    printf("%.*g\n", 17, 1.0/10.0 + 2.0/10.0 - 3.0/10.0); // 5.5511151231257827e-17
    printf("%.*g\n", 17, 1.0/10.0);                       // 0.10000000000000001
    return 0;
}
